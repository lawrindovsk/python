## Operadores Lógicos
#Operador Lógicos representam saídas verdadeiras ou falsos, boolean.
# Maior que (>)
print("8 é maior que 4?:",8>4) # True
print("4 é maior que 8?:", 4>8) # False


#Maior igual (>=)
print("8 é maior ou igual a 4?:",8>=4) # True
print("4 é maior ou igual a 8?:",4>=8) # False
#Menor que (<)
print("5 é menor que 6?:",5<6) # True
print("10 é menor que 10?:",10<10) # False

#Menor igual (<=)
print("20 é menor ou igual a 21?:",20<=21) # True
print("21 é menor ou igual a 20?:",21<=20) # False

#Igualdade (==)
print("5 é igual a 5?:",5==5) # True
print("5 é igual a 6?",5==6) # False

#Diferente (!=)
print("5 é diferente ou igual a 6?",5!=6) # True
print("5 é diferente ou igual a 5?",5!=5) # False
